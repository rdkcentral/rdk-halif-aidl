  if (UNLIKELY(_aidl_ret_status == ::android::UNKNOWN_TRANSACTION && IDeviceInfo::getDefaultImpl())) {
     return IDeviceInfo::getDefaultImpl()->getCapabilities(_aidl_return);
  }
